//For Loop
for(let a = 0; a <= 8; a++){
    console.log(a);
}

// Perulangan While

let a = 1
while (a <= 100) {
    console.log(a);
    a++
}

// Perulangan do While
let i = 1;

do {
  console.log(i);
  i++;
} while (i <= 100);

// Infinite Loop
let b = 1;
 
while (b <= 5) {
  console.log(b);
}

